export function formatMessage(template: string, values: Record<string, string | number>) {
  return template.replace(/\{(\w+)\}/g, (_, key: string) => String(values[key] ?? ""));
}

function stripItemSuffix(name: string) {
  return name.replace(/ \[[^\]]+\]$/, "");
}

function getItemSuffix(name: string) {
  return name.match(/ \[([^\]]+)\]$/)?.[1] || "";
}

export function formatTag(tag: string | undefined, locale: Locale) {
  if (!tag) {
    return "";
  }

  return TAG_LABELS[locale][tag] || tag;
}

export function formatItemName(item: Pick<MarketItem, "name">, locale: Locale) {
  if (locale === "en") {
    return item.name;
  }

  const baseName = stripItemSuffix(item.name);
  const translated = ITEM_NAME_TRANSLATIONS[locale][baseName] || baseName;
  const suffix = getItemSuffix(item.name);
  return suffix ? `${translated} [${formatTag(suffix, locale)}]` : translated;
}

export function formatNumber(value: number, digits = 2, locale: Locale = DEFAULT_MARKET_LOCALE) {
  if (!Number.isFinite(value)) {
    return "--";
  }

  return new Intl.NumberFormat(locale, {
    minimumFractionDigits: 0,
    maximumFractionDigits: digits
  }).format(value);
}

export function formatPercent(value: number, locale: Locale = DEFAULT_MARKET_LOCALE) {
  if (!Number.isFinite(value)) {
    return "--";
  }

  return `${value >= 0 ? "+" : ""}${formatNumber(value, 2, locale)}%`;
}

export function formatDate(
  value: string | undefined,
  locale: Locale = DEFAULT_MARKET_LOCALE,
  t: UiText = UI_TEXT[DEFAULT_MARKET_LOCALE]
) {
  if (!value) {
    return t.noSnapshot;
  }

  const date = new Date(value);
  if (!Number.isFinite(date.getTime())) {
    return value.slice(0, 19).replace("T", " ");
  }

  return new Intl.DateTimeFormat(locale, {
    month: "short",
    day: "numeric",
    hour: "2-digit",
    minute: "2-digit"
  }).format(date);
}
